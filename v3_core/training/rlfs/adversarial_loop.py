import torch
import torch.nn as nn
from loguru import logger

class BiDirectionalVerifier(nn.Module):
    """
    SNAP-C1 V3 Model Component: Adversarial Execution Verifier
    
    This essentially replaces the Ground Truth requirement in `ast_evaluator`.
    Instead of checking the generated AST against a known Human dataset, this Dual-Network 
    system runs adversarial Self-Play entirely in VRAM.
    
    1. Generator writes a block of code (AST) based on a Prompt.
    2. Verifier reads the AST and tries to guess what the original Prompt was.
    3. If the Verifier fails to reconstruct the Prompt, the Generator's logic is punished.
    """
    def __init__(self, d_model: int = 1024, ast_vocab_size: int = 250):
        super().__init__()
        self.d_model = d_model
        
        # Symmetrical GNN Reader to ingest the Generator's AST
        self.ast_embedder = nn.Embedding(ast_vocab_size, d_model)
        self.tree_rnn = nn.GRU(
            input_size=d_model, 
            hidden_size=d_model, 
            num_layers=2,
            batch_first=True,
            bidirectional=True
        )
        
        # Project the Bi-directional hidden state back down to 1024 concepts
        self.concept_proj = nn.Linear(d_model * 2, d_model)
        
    def forward(self, generated_ast: torch.Tensor) -> torch.Tensor:
        """
        Takes the mathematical AST tree [batch, seq_len] and compresses it 
        back into a 1024-dimension Holo-Concept.
        """
        # Embed the mathematical nodes into continuous space
        embedded_tree = self.ast_embedder(generated_ast)
        
        # Compress the tree into a single thought vector
        _, final_hidden = self.tree_rnn(embedded_tree)
        
        # Extract the last layer's hidden states for both directions
        forward_hidden = final_hidden[-2]
        backward_hidden = final_hidden[-1]
        
        # Cat them together and project to original 1024 Hologram
        combined = torch.cat([forward_hidden, backward_hidden], dim=-1)
        reconstructed_concept = self.concept_proj(combined)
        
        return reconstructed_concept

class AdversarialSelfPlay(nn.Module):
    """
    The Master Training Loop for V3.
    Requires ZERO subprocesses and potentially ZERO supervised human data.
    """
    def __init__(self, d_model: int = 1024):
        super().__init__()
        # In a real training script, these would be the actual parameters
        # self.v3_assembly = V3GenerativeReasoningArchitecture()
        self.verifier = BiDirectionalVerifier(d_model)
        
        # Loss function to measure the "semantic distance" between intended and reconstructed prompts
        self.cosine_loss = nn.CosineEmbeddingLoss()
        
    def forward(self, original_hologram: torch.Tensor, generated_ast: torch.Tensor) -> torch.Tensor:
        """
        Calculates the internal adversarial penalty score.
        """
        # 1. Ask the secondary network to "read" the code and guess the prompt
        reconstructed_hologram = self.verifier(generated_ast)
        
        # 2. Check if the code accurately represents the prompt!
        # A target of 1 means they should point in the exact same mathematical direction
        target = torch.ones(original_hologram.size(0)).to(original_hologram.device)
        
        adversarial_penalty = self.cosine_loss(reconstructed_hologram, original_hologram, target)
        
        return adversarial_penalty

